    private void BtnDividirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnDividirActionPerformed
        if(!operador[0].equals("/") && operador[1].equals("=")){
            setearValor1("/");
            return;
        }
        
        if(!operador[0].equals("")  && !operador[0].equals("/"))
        {
            operador[0] = "/";
            return;
        }
        
        banderaDivision++;
        if(banderaDivision > 1){
            LimpiarTexto();
            return;
        }
        
        setearValor1("/");
    }//GEN-LAST:event_BtnDividirActionPerformed

    private void setearValor1(String operadorValor){
        numero1 = Double.parseDouble(TxtResultado.getText());
        operador[0] = operadorValor;
        LimpiarTexto();
    }
    
    
