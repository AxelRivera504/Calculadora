    private void verificarSetearResultado(double resultado){
        try{
            String resultadoString = String.valueOf(resultado);
            String[] resultadoArray = resultadoString.split("\\.");
            if(resultadoArray[1].equals("0"))
                TxtResultado.setText(String.valueOf(resultadoArray[0]));
            else
                TxtResultado.setText(String.valueOf(resultado));
        }catch(Exception e){
            TxtResultado.setText(String.valueOf(resultado));
        }
    }
