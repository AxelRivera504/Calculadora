     void agregarNumero(String numero){
        if(TxtResultado.getText().charAt(0) == '0' && !numero.equals(".") && !TxtResultado.getText().contains("."))
            TxtResultado.setText("");
        
        TxtResultado.setText(TxtResultado.getText() + numero);
    }
    
