    private void BtnIgualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnIgualActionPerformed
        banderaResta = 0;
        banderaSuma = 0;
        banderaMultiplicacion = 0;
        banderaDivision = 0;
        
        System.out.println(operador[0]);
        System.out.println(TxtResultado.getText());
        numero2 = Double.parseDouble(TxtResultado.getText());
        switch(operador[0]){
            case "+":
            resultado = numero1 + numero2;
            verificarSetearResultado(resultado);
            operador[1] = "=";
            break;
            case "-":
            resultado = numero1 - numero2;
            verificarSetearResultado(resultado);
            operador[1] = "=";
            break;
            case "X":
            resultado = numero1 * numero2;
            verificarSetearResultado(resultado);
            operador[1] = "=";
            break;
            case "/":
            if(numero2 == 0)
            TxtResultado.setText("Error");
            else{
                resultado = numero1 / numero2;
                verificarSetearResultado(resultado);
                operador[1] = "=";
            }
            break;
            case "%":
            resultado = (numero1 / numero2) * 100;
            verificarSetearResultado(resultado);
            operador[1] = "=";
            break;
            default:
            break;
        }

    }//GEN-LAST:event_BtnIgualActionPerformed

